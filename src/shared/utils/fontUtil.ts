export type FontWeight =
  | '100'
  | '200'
  | '300'
  | '400'
  | '500'
  | '600'
  | '700'
  | '800'
  | '900';

export const parseFontWeight = (value: string): FontWeight => {
  const numericWeights: Record<string, FontWeight> = {
    '100': '100',
    '200': '200',
    '300': '300',
    '400': '400',
    '500': '500',
    '600': '600',
    '700': '700',
    '800': '800',
    '900': '900'
  };
  return numericWeights[value] || value;
};
